public class While
{  
	public static void main(String[] args)
	{  
		int i=1;  
	    	while(i<=10)
		{  
			System.out.println(i);  
    			i++;  
    		}
    		int x = 10;
      		do
      		{
         		System.out.print("value of x : " + x );
         		x++;
         		System.out.print("\n");
      		}while(x<20);  
	}  
}  
